    public ArrayList<Esma> getRangedList(){
        ArrayList<Esma> tmpList = new ArrayList<>();

        for(Esma e : list){
            if(e.getNumber() >= MainActivity.beginningIndex && e.getNumber() <= MainActivity.endingIndex){
                tmpList.add(e);
            }
        }

        tmpList.sort(new EsmaComparatorNumber());
        return tmpList;
    }
    public ArrayList<Esma> getRandomList(){
        ArrayList<Esma> pickedList = new ArrayList<>();

        int count = 4;

        for(int i = 0; i<count;i++){
            while(true){
                Esma esm = getRandom();
                if(!listContainsEsma(pickedList,esm)){
                    pickedList.add(esm);
                    break;
                }
            }
        }

        return pickedList;
    }
    public ArrayList<Esma> getRandomListFromRange(){
        ArrayList<Esma> pickedList = new ArrayList<>();

        int count = 4;

        for(int i = 0; i<count;i++){
            while(true){
                Esma esm = getRandom();
                if(!listContainsEsma(pickedList,esm) && esm.isInRange()){
                    pickedList.add(esm);
                    break;
                }
            }
        }

        return pickedList;
    }

    public void resetStats(){
        for(Esma e: list){
            e.resetStat();
        }
    }

    public boolean listContainsEsma(ArrayList<Esma> list, Esma esm){
        for (Esma e:list) {
            if(e.isSame(esm)){
                return true;
            }
        }

        return false;
    }

    public void increaseWrongOfNr(int nr){
        for(Esma e : list){
            if(e.getNumber() == nr){
                e.increaseWrong();
                break;
            }
        }
    }

    public void increaseRightOfNr(int nr){
        for(Esma e : list){
            if(e.getNumber() == nr){
                e.increaseRight();
                break;
            }
        }
    }

